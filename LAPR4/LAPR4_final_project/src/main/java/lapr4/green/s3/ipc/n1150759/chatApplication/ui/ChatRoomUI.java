    @Override
    public void updateChat(Message message) {
        String timeStamp = new SimpleDateFormat("HH:mm").format(new Date());
        this.areaChat.append(timeStamp + " - " + 
                chatCtrl.extensionConnectionManager().
                        findProfileNickByIP(message.messageSender().
                                getAddress()) + ": " + message.message() + "\n");
        
        /**
         * Auto scrolls the chat area to the bottom
         */
        this.areaChat.setCaretPosition(this.areaChat.getDocument().getLength());
    }
