    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        Set<NetworkAddress> receivers = this.chat.allOtherUsersInChat();
        
        // Creates the message
        if (areaMsg.getText().isEmpty())
            JOptionPane.showMessageDialog(null, "Message can't be empty!", 
                    "Error", JOptionPane.ERROR_MESSAGE);
        Message message = this.sendMsgCtrl.createMessage(areaMsg.getText(), 
                Utils.ownIpAddress(), receivers);
        // Defines the message
        this.sendMsgCtrl.addMessageToSend(message);
        // Sends the message to all receivers
        if (this.sendMsgCtrl.sendMessageToChatRoom())
            this.areaMsg.setText(null);
        
        this.areaMsg.requestFocus();
    }//GEN-LAST:event_btnSendActionPerformed

