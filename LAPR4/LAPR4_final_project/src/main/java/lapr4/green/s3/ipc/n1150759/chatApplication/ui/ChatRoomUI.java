    private void btnLeaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLeaveActionPerformed
        if (this.chat.leaveChatRoom(Utils.ownIpAddress())) {
            JOptionPane.showMessageDialog(null, "You left the chat room.");
            if (this.chat.allUsersInChat().isEmpty()) {
                this.chatCtrl.deleteChatRoom(this.chat);
                JOptionPane.showMessageDialog(null, "The chat room was deleted.");
            }
            dispose();
        } else
            JOptionPane.showMessageDialog(null, "Couldn't leave the chat room.");
    }//GEN-LAST:event_btnLeaveActionPerformed

