    private void confirmClose() {
        if (!txtFieldTag.getText().isEmpty()) {
            int confirmed = JOptionPane.showConfirmDialog(null, "Do you really want to cancel? All data will be lost", "Cancel", JOptionPane.YES_NO_OPTION);
            if (confirmed == JOptionPane.YES_OPTION) {
                dispose();
            }
        } else {
            dispose();
        }
    }
