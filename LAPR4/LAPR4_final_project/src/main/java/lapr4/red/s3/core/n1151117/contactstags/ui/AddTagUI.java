    private void btnAddTagActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddTagActionPerformed
        tag = txtFieldTag.getText();
        Tag t = new Tag(tag);
        if(ctrl.associateTagToContact(contact, t)){
            try {
                ctrl.updateContact(contact, contact.name(), contact.firstName(), contact.lastName());
                JOptionPane.showMessageDialog(null, "Tag added with success", "Success", JOptionPane.INFORMATION_MESSAGE);
                dispose();
            } catch (DataConcurrencyException | DataIntegrityViolationException ex) {
                Logger.getLogger(AddTagUI.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Error associating tag to contact", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "The tag is already associated to the contact", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAddTagActionPerformed

